export class User {

    constructor (id_user, nombre, usuario, email, password, provincia, descripcion, foto){

        this.id_user = id_user;
        this.nombre = nombre;
        this.usuario = usuario;
        this.email = email;
        this.password = password;
        this.provincia = provincia;
        this.descripcion = descripcion;
        this.foto = foto;
    }

};

